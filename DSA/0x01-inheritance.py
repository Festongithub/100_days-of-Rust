#!/usr/bin/python3

class Dog:
    """
    A simple class representation of dog 
    """

    def __init__(self, name, age,):
        self.name = name
        self.age = age

    def bark(self):
        print("{} barks".format(self.name))

    def get_age(self):
        print("{} is {} years old".format(self.name, self.age))

    def check(self):
        return "{}".format(self.name)

spoty = Dog("Spotty", "78")
spoty.bark()
spoty.get_age()
spoty.check()



class Puppy(Dog):

    """
    An inherited class of the dog
    """

    def Print(self):
        print("I am from my home")

puppy = Dog("scooby", "56")

puppy.bark()
puppy.get_age()
puppy.check()
